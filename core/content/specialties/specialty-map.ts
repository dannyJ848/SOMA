// =============================================================================
// LOOKUP INDEXES (built once at load time)
// =============================================================================

const _specialtyById = new Map<string, MedicalSpecialty>();
const _specialtiesByCondition = new Map<string, MedicalSpecialty[]>();
const _specialtiesByRegion = new Map<AnatomyRegion, MedicalSpecialty[]>();
const _specialtiesBySystem = new Map<BodySystemFocus, MedicalSpecialty[]>();

function _buildIndexes(): void {
  for (const spec of MEDICAL_SPECIALTIES) {
    _specialtyById.set(spec.specialtyId, spec);

    for (const condId of spec.commonConditions) {
      const list = _specialtiesByCondition.get(condId) ?? [];
      list.push(spec);
      _specialtiesByCondition.set(condId, list);
    }

    for (const region of spec.anatomyRegions) {
      const list = _specialtiesByRegion.get(region) ?? [];
      list.push(spec);
      _specialtiesByRegion.set(region, list);
    }

    for (const system of spec.bodySystemsFocus) {
      const list = _specialtiesBySystem.get(system) ?? [];
      list.push(spec);
      _specialtiesBySystem.set(system, list);
    }
  }
}

_buildIndexes();

