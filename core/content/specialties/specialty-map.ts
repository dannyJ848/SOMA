/**
 * Get a single specialty by its ID.
 * Returns undefined if not found.
 */
export function getSpecialty(specialtyId: string): MedicalSpecialty | undefined {
  return _specialtyById.get(specialtyId);
}

/**
 * Get all specialties that list a given conditionId among their common conditions.
 * Returns an empty array if no specialty covers the condition.
 */
export function getSpecialtiesForCondition(conditionId: string): MedicalSpecialty[] {
  return _specialtiesByCondition.get(conditionId) ?? [];
}

/**
 * Get all specialties relevant to a given anatomy region.
 * Returns an empty array if no specialty maps to that region.
 */
export function getSpecialtiesForRegion(region: AnatomyRegion): MedicalSpecialty[] {
  return _specialtiesByRegion.get(region) ?? [];
}

/**
 * Get all specialties that focus on a given body system.
 * Returns an empty array if no specialty covers that system.
 */
export function getSpecialtiesForBodySystem(system: BodySystemFocus): MedicalSpecialty[] {
  return _specialtiesBySystem.get(system) ?? [];
}

/**
 * Get all specialties in a given category.
 */
export function getSpecialtiesByCategory(category: MedicalSpecialty['category']): MedicalSpecialty[] {
  return MEDICAL_SPECIALTIES.filter((s) => s.category === category);
}

/**
 * Search specialties by name (case-insensitive, matches English or Spanish name).
 */
export function searchSpecialties(query: string): MedicalSpecialty[] {
  const q = query.toLowerCase();
  return MEDICAL_SPECIALTIES.filter(
    (s) => s.name.toLowerCase().includes(q) || s.spanishName.toLowerCase().includes(q)
  );
}
