/**
 * Translate a single English medical term to Spanish.
 * Returns the original term if no translation is found.
 */
export function translateTerm(term: string): string {
  const key = term.toLowerCase();
  if (key in MEDICAL_TERMS_ES) {
    return MEDICAL_TERMS_ES[key];
  }
  // Try case-insensitive lookup against all keys
  for (const [k, v] of Object.entries(MEDICAL_TERMS_ES)) {
    if (k.toLowerCase() === key) {
      return v;
    }
  }
  return term;
}
